/**
 * –ü–µ—Ä–µ—Å—Ç–∞–Ω–æ–≤–∫–∞ –ø–æ DI
–î–∞–Ω–∞ —Å—Ç—Ä–æ–∫–∞ S –ø—Ä–æ–∏–∑–≤–æ–ª—å–Ω–æ–π –¥–ª–∏–Ω—ã N, –≤ –∫–æ—Ç–æ—Ä–æ–π —Å—É—â–µ—Å—Ç–≤—É–µ—Ç —Ç–æ–ª—å–∫–æ –¥–≤–∞ —Å–∏–º–≤–æ–ª–∞:
I (increase),
D (decrease).
–ù–µ–æ–±—Ö–æ–¥–∏–º–æ —Ä–µ–∞–ª–∏–∑–æ–≤–∞—Ç—å —Ç–∞–∫—É—é –ø–µ—Ä–µ—Å—Ç–∞–Ω–æ–≤–∫—É A, —á—Ç–æ–±—ã –¥–ª—è –≤—Å–µ—Ö i=[0, 1, ..., N - 1] –±—ã–ª–∏ –≤–µ—Ä–Ω—ã —Å–ª–µ–¥—É—é—â–∏–µ –ø—Ä–∞–≤–∏–ª–∞:
–ï—Å–ª–∏ S[i] === 'I' ‚áí A[i] < A[i+1];
–ï—Å–ª–∏ S[i] === 'D' ‚áí A[i] > A[i+1].
–°–ª–æ–∂–Ω–æ—Å—Ç—å –∞–ª–≥–æ—Ä–∏—Ç–º–∞ –ø–æ –≤—Ä–µ–º–µ–Ω–∏ ‚Äî O(n).
 */

function match(str) {
  // –ö–æ–¥ –∑–¥–µ—Å—å
  let l = 0
  let r = str.length
  const result = []

  for(let i=0; i < str.length; i++) {
    if (str.charAt(i) === 'I') {
      result.push(l)
      l++
    } else 
    if (str.charAt(i) === 'D')  {
      result.push(r)
      r--
    }
  }

  result.push(l)

  // console.log("üöÄ ~ result:", result)
  return result
}

match('III') // [0,1,2,3]
match('DDI') // [3,2,0,1]
match('IDID') // [0,4,1,3,2]
match('') // [0,4,1,3,2]